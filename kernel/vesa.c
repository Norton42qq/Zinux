void vesa_set_palette(uint8_t index, uint8_t r, uint8_t g, uint8_t b) {
    outb(0x3C8, index);
    outb(0x3C9, r >> 2);
    outb(0x3C9, g >> 2);
    outb(0x3C9, b >> 2);
}

void vesa_get_palette(uint8_t index, uint8_t* r, uint8_t* g, uint8_t* b) {
    outb(0x3C7, index);
    if(r) *r = inb(0x3C9) << 2;
    if(g) *g = inb(0x3C9) << 2;
    if(b) *b = inb(0x3C9) << 2;
}

