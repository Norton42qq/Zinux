// инициализация через BIOS
int vesa_init(void) {
    uint32_t lfb = *LFB_ADDR_PTR;
    if (lfb != 0 && lfb != 0xFFFFFFFF) {
        framebuffer = (uint8_t*)lfb;
    }

    vesa_init_palette();
    vesa_clear(COLOR_BLACK);
    return 0;
}

