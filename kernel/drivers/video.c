static int       g_bytes_pp = 4;     // байт на пиксель

// UTF-8 -> CP866 (Для отображения русских букв без использования escape последовательностей)
static uint8_t utf8_to_cp866(const uint8_t* s, int* adv) {
    uint8_t b0 = s[0];
    *adv = 1;

    if (b0 < 0x80) return b0;

    if (b0 == 0xD0) {
        uint8_t b1 = s[1];
        if (!b1) return '?';
        *adv = 2;
        if (b1 == 0x81) return 0xF0;                 // Ё */
        if (b1 >= 0x90 && b1 <= 0xAF) return (uint8_t)(0x80 + (b1 - 0x90)); // А..Я */
        if (b1 >= 0xB0 && b1 <= 0xBF) return (uint8_t)(0xA0 + (b1 - 0xB0)); // а..п */
        return '?';
    }

    if (b0 == 0xD1) {
        uint8_t b1 = s[1];
        if (!b1) return '?';
        *adv = 2;
        if (b1 == 0x91) return 0xF1;                 // ё */
        if (b1 >= 0x80 && b1 <= 0x8F) return (uint8_t)(0xE0 + (b1 - 0x80)); // р..я */
        return '?';
    }

    return '?';
}

// Текстовый размер экрана
int VIDEO_TEXT_COLS = 80;
int VIDEO_TEXT_ROWS = 25;

// Упаковка цвета под нативный формат
static inline uint32_t color_pack(uint32_t rgb) {
    uint8_t  r = (rgb >> 16) & 0xFF;
    uint8_t  g = (rgb >>  8) & 0xFF;
    uint8_t  b = (rgb      ) & 0xFF;
    switch (g_vi.bpp) {
        case 32:
        case 24:
            return ((uint32_t)r << g_vi.red_pos)
                 | ((uint32_t)g << g_vi.green_pos)
                 | ((uint32_t)b << g_vi.blue_pos);
        case 16: {
            uint32_t rv = (r >> (8 - g_vi.red_size))   & ((1 << g_vi.red_size)   - 1);
            uint32_t gv = (g >> (8 - g_vi.green_size)) & ((1 << g_vi.green_size) - 1);
            uint32_t bv = (b >> (8 - g_vi.blue_size))  & ((1 << g_vi.blue_size)  - 1);
            return (rv << g_vi.red_pos) | (gv << g_vi.green_pos) | (bv << g_vi.blue_pos);
        }
        case 15: {
            uint32_t rv = (r >> 3) & 0x1F;
            uint32_t gv = (g >> 3) & 0x1F;
            uint32_t bv = (b >> 3) & 0x1F;
            return (rv << 10) | (gv << 5) | bv;
        }
        case 8:
        default:
            return ((r >> 5) << 5) | ((g >> 5) << 2) | (b >> 6);
    }
}

// Запись пикселя в framebuffer
static inline void fb_write_pixel(int x, int y, uint32_t native) {
    uint8_t* ptr = g_fb + (uint32_t)y * g_vi.pitch + (uint32_t)x * g_bytes_pp;
    switch (g_vi.bpp) {
        case 32:
            *((volatile uint32_t*)ptr) = native;
            break;
        case 24:
            ptr[0] = (native      ) & 0xFF;
            ptr[1] = (native >>  8) & 0xFF;
            ptr[2] = (native >> 16) & 0xFF;
            break;
        case 15:
        case 16:
            *((volatile uint16_t*)ptr) = (uint16_t)native;
            break;
        case 8:
        default:
            *ptr = (uint8_t)native;
            break;
    }
}

// Чтение пикселя из framebuffer
static inline uint32_t fb_read_pixel(int x, int y) {
    const uint8_t* ptr = g_fb + (uint32_t)y * g_vi.pitch + (uint32_t)x * g_bytes_pp;
    switch (g_vi.bpp) {
        case 32: return *((const volatile uint32_t*)ptr);
        case 24: return (uint32_t)ptr[0] | ((uint32_t)ptr[1]<<8) | ((uint32_t)ptr[2]<<16);
        case 15:
        case 16: return *((const volatile uint16_t*)ptr);
        case 8:  return *ptr;
        default: return 0;
    }
}

// video_init
int video_init(void) {
    // Загрузчик кладёт VideoInfo по адресу VIDEO_INFO_ADDR
    const VideoInfo* src = (const VideoInfo*)VIDEO_INFO_ADDR;


    if (src->lfb_addr == 0 || src->lfb_addr == 0xFFFFFFFF) return -1;
    if (src->width == 0 || src->height == 0)               return -1;
    if (src->bpp != 8 && src->bpp != 15 && src->bpp != 16
        && src->bpp != 24 && src->bpp != 32)               return -1;

    g_vi = *src;
    g_fb = (uint8_t*)g_vi.lfb_addr;


    // Байт на пиксель
    g_bytes_pp = (g_vi.bpp + 7) / 8;

    // Заполнить поля маски для 8bpp если загрузчик не заполнил их
    if (g_vi.bpp == 8) {
        g_vi.red_pos   = 5; g_vi.red_size   = 3;
        g_vi.green_pos = 2; g_vi.green_size  = 3;
        g_vi.blue_pos  = 0; g_vi.blue_size   = 2;
    }

    VIDEO_TEXT_COLS = g_vi.width  / 8;
    VIDEO_TEXT_ROWS = g_vi.height / 16;

    video_clear(COLOR_BLACK);
    return 0;
}

const VideoInfo* video_get_info(void) { return &g_vi; }

// Пиксели
void video_pixel(int x, int y, uint32_t color) {
    if ((unsigned)x >= (unsigned)g_vi.width ||
        (unsigned)y >= (unsigned)g_vi.height) return;
    fb_write_pixel(x, y, color_pack(color));
}

uint32_t video_getpixel(int x, int y) {
    if ((unsigned)x >= (unsigned)g_vi.width ||
        (unsigned)y >= (unsigned)g_vi.height) return 0;
    return fb_read_pixel(x, y);
}

void video_clear(uint32_t color) {
    uint32_t native = color_pack(color);
    if (g_vi.bpp == 32) {
        // Быстрый путь 32bpp: заполняем построчно через uint32_t
        for (int y = 0; y < g_vi.height; y++) {
            uint32_t* row = (uint32_t*)(g_fb + (uint32_t)y * g_vi.pitch);
            for (int x = 0; x < g_vi.width; x++) row[x] = native;
        }
    } else if (g_vi.bpp == 16 || g_vi.bpp == 15) {
        // Быстрый путь 16/15bpp
        for (int y = 0; y < g_vi.height; y++) {
            uint16_t* row = (uint16_t*)(g_fb + (uint32_t)y * g_vi.pitch);
            for (int x = 0; x < g_vi.width; x++) row[x] = (uint16_t)native;
        }
    } else {
        // Универсальный путь для 8/24bp
        for (int y = 0; y < g_vi.height; y++)
            for (int x = 0; x < g_vi.width; x++)
                fb_write_pixel(x, y, native);
    }
}

// Примитивы
void video_hline(int x, int y, int len, uint32_t color) {
    if ((unsigned)y >= (unsigned)g_vi.height) return;
    uint32_t native = color_pack(color);
    int x2 = x + len;
    if (x  < 0)           x  = 0;
    if (x2 > g_vi.width)  x2 = g_vi.width;
    if (x >= x2) return;
    // Быстрые пути для распространённых bpp
    if (g_vi.bpp == 32) {
        uint32_t* row = (uint32_t*)(g_fb + (uint32_t)y * g_vi.pitch);
        for (int i = x; i < x2; i++) row[i] = native;
    } else if (g_vi.bpp == 16 || g_vi.bpp == 15) {
        uint16_t* row = (uint16_t*)(g_fb + (uint32_t)y * g_vi.pitch);
        for (int i = x; i < x2; i++) row[i] = (uint16_t)native;
    } else {
        for (int i = x; i < x2; i++) fb_write_pixel(i, y, native);
    }
}

void video_vline(int x, int y, int len, uint32_t color) {
    if ((unsigned)x >= (unsigned)g_vi.width) return;
    uint32_t native = color_pack(color);
    int y2 = y + len;
    if (y  < 0)            y  = 0;
    if (y2 > g_vi.height)  y2 = g_vi.height;
    for (int i = y; i < y2; i++) fb_write_pixel(x, i, native);
}

void video_line(int x0, int y0, int x1, int y1, uint32_t color) {
    int dx = x1 - x0; if (dx < 0) dx = -dx;
    int dy = y1 - y0; if (dy < 0) dy = -dy;
    int sx = (x0 < x1) ? 1 : -1;
    int sy = (y0 < y1) ? 1 : -1;
    int err = dx - dy;
    for (;;) {
        video_pixel(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

void video_rect(int x, int y, int w, int h, uint32_t color) {
    video_hline(x, y,         w, color);
    video_hline(x, y + h - 1, w, color);
    video_vline(x,         y, h, color);
    video_vline(x + w - 1, y, h, color);
}

void video_fillrect(int x, int y, int w, int h, uint32_t color) {
    for (int i = 0; i < h; i++) video_hline(x, y + i, w, color);
}

void video_circle(int cx, int cy, int r, uint32_t color) {
    int px = 0, py = r, d = 3 - 2 * r;
    while (py >= px) {
        video_pixel(cx+px, cy+py, color); video_pixel(cx-px, cy+py, color);
        video_pixel(cx+px, cy-py, color); video_pixel(cx-px, cy-py, color);
        video_pixel(cx+py, cy+px, color); video_pixel(cx-py, cy+px, color);
        video_pixel(cx+py, cy-px, color); video_pixel(cx-py, cy-px, color);
        px++;
        d = (d > 0) ? (d + 4*(px - py--) + 10) : (d + 4*px + 6);
    }
}

void video_fillcircle(int cx, int cy, int r, uint32_t color) {
    for (int dy = -r; dy <= r; dy++)
        for (int dx = -r; dx <= r; dx++)
            if (dx*dx + dy*dy <= r*r)
                video_pixel(cx+dx, cy+dy, color);
}

// Текст
void video_putchar(int col, int row, char c, uint32_t fg, uint32_t bg) {
    unsigned char uc = (unsigned char)c;
    uint32_t nat_fg = color_pack(fg);
    uint32_t nat_bg = color_pack(bg);
    const uint8_t* glyph = font_data[uc];
    int px0 = col * 8;
    int py0 = row * 16;
    for (int r = 0; r < 16; r++) {
        uint8_t bits = glyph[r];
        for (int b = 0; b < 8; b++) {
            int px = px0 + b;
            int py = py0 + r;
            if ((unsigned)px >= (unsigned)g_vi.width ||
                (unsigned)py >= (unsigned)g_vi.height) continue;
            fb_write_pixel(px, py, (bits & (0x80 >> b)) ? nat_fg : nat_bg);
        }
    }
}

void video_puts(int col, int row, const char* str, uint32_t fg, uint32_t bg) {
    int c = col;
    const uint8_t* s = (const uint8_t*)str;
    while (*s) {
        int adv = 1;
        uint8_t ch = utf8_to_cp866(s, &adv);
        video_putchar(c++, row, (char)ch, fg, bg);
        s += adv;
    }
}

// Внутренний скролл
static void scroll_up(void) {
    uint32_t line_bytes = (uint32_t)g_vi.pitch * 16u;
    uint8_t* dst        = g_fb;
    uint8_t* src        = g_fb + line_bytes;
    uint32_t copy_size  = (uint32_t)g_vi.pitch * (uint32_t)(g_vi.height - 16);
    memmove(dst, src, (size_t)copy_size);
    uint32_t nat_bg = color_pack(g_bg);
    for (int y = g_vi.height - 16; y < g_vi.height; y++)
        for (int x = 0; x < g_vi.width; x++)
            fb_write_pixel(x, y, nat_bg);
}

void video_print_char(char c) {
    if (c == '\n') {
        g_cur_x = 0;
        if (++g_cur_y >= VIDEO_TEXT_ROWS) { scroll_up(); g_cur_y = VIDEO_TEXT_ROWS - 1; }
    } else if (c == '\b') {
        if (g_cur_x > 0) {
            g_cur_x--;
            video_putchar(g_cur_x, g_cur_y, ' ', g_fg, g_bg);
        }
    } else if (c == '\r') {
        g_cur_x = 0;
    } else if ((unsigned char)c >= 32) {
        video_putchar(g_cur_x, g_cur_y, c, g_fg, g_bg);
        if (++g_cur_x >= VIDEO_TEXT_COLS) {
            g_cur_x = 0;
            if (++g_cur_y >= VIDEO_TEXT_ROWS) { scroll_up(); g_cur_y = VIDEO_TEXT_ROWS - 1; }
        }
    }
}

void video_print(const char* str) {
    const uint8_t* s = (const uint8_t*)str;
    while (*s) {
        int adv = 1;
        uint8_t ch = utf8_to_cp866(s, &adv);
        video_print_char((char)ch);
        s += adv;
    }
}

void video_println(const char* str) {
    video_print(str);
    video_print_char('\n');
}

void video_backspace(void) { video_print_char('\b'); }

void video_set_cursor(int col, int row) { g_cur_x = col; g_cur_y = row; }
void video_get_cursor(int* col, int* row) {
    if (col) *col = g_cur_x;
    if (row) *row = g_cur_y;
}

void video_set_color(uint32_t fg, uint32_t bg) { g_fg = fg; g_bg = bg; }

// Утилиты
void video_vsync(void) {
    while ( inb(0x3DA) & 8);
    while (~inb(0x3DA) & 8);
}

int  video_width(void)  { return g_vi.width;  }
int  video_height(void) { return g_vi.height; }

void video_put_dec(uint32_t n) {
    if (n == 0) { video_print_char('0'); return; }
    char buf[12]; int i = 0;
    while (n > 0) { buf[i++] = '0' + (n % 10); n /= 10; }
    while (i > 0)  video_print_char(buf[--i]);
}

void video_put_hex(uint32_t n) {
    const char hex[] = "0123456789ABCDEF";
    video_print("0x");
    for (int i = 28; i >= 0; i -= 4)
        video_print_char(hex[(n >> i) & 0xF]);
}

