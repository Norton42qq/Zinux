// Установить цвет для записи в Write Mode 0 (Set/Reset)
static inline void gc_set_color(uint8_t color) {
    outb(GC_ADDR, GC_SET_RESET);  outb(GC_DATA, color & 0x0F);
    outb(GC_ADDR, GC_ENABLE_SR);  outb(GC_DATA, 0x0F);
}

// Установить битовую маску
static inline void gc_set_bitmask(uint8_t mask) {
    outb(GC_ADDR, GC_BIT_MASK);   outb(GC_DATA, mask);
}

// Сброс в режим по умолчанию
static inline void gc_reset(void) {
    outb(GC_ADDR, GC_ENABLE_SR);  outb(GC_DATA, 0x00);
    outb(GC_ADDR, GC_BIT_MASK);   outb(GC_DATA, 0xFF);
    outb(GC_ADDR, GC_MODE);       outb(GC_DATA, 0x00);
    outb(SEQ_ADDR, SEQ_MAP_MASK); outb(SEQ_DATA, 0x0F);
}

static void draw_char(int col, int row, unsigned char c) {
    uint32_t base = (uint32_t)(row * FONT_H * BYTES_PER_ROW) + col;
    const uint8_t* glyph = font8x16[(uint8_t)c];

    outb(GC_ADDR, GC_MODE); outb(GC_DATA, 0x00);

    for (int dy = 0; dy < FONT_H; dy++) {
        uint8_t bits = glyph[dy];
        uint32_t addr = base + (uint32_t)(dy * BYTES_PER_ROW);

        if (bits != 0) {
            gc_set_color(fg_color);
            gc_set_bitmask(bits);
            volatile uint8_t dummy = framebuf[addr]; (void)dummy;
            framebuf[addr] = 0xFF;
        }

        uint8_t bg_bits = ~bits;
        if (bg_bits != 0) {
            gc_set_color(bg_color);
            gc_set_bitmask(bg_bits);
            volatile uint8_t dummy = framebuf[addr]; (void)dummy;
            framebuf[addr] = 0xFF;
        }
    }

    gc_reset();
}

static void erase_char(int col, int row) {
    uint32_t base = (uint32_t)(row * FONT_H * BYTES_PER_ROW) + col;

    outb(GC_ADDR, GC_MODE);    outb(GC_DATA, 0x00);
    gc_set_color(bg_color);
    gc_set_bitmask(0xFF);

    for (int dy = 0; dy < FONT_H; dy++) {
        uint32_t addr = base + (uint32_t)(dy * BYTES_PER_ROW);
        volatile uint8_t dummy = framebuf[addr]; (void)dummy;
        framebuf[addr] = 0xFF;
    }
    gc_reset();
}

// ===================
// Скролл
// ===================
static void scroll_up(void) {
    uint32_t src  = (uint32_t)(FONT_H * BYTES_PER_ROW);
    uint32_t size = (uint32_t)((ROWS - 1) * FONT_H * BYTES_PER_ROW);

    outb(GC_ADDR, GC_MODE);    outb(GC_DATA, 0x01);
    outb(SEQ_ADDR, SEQ_MAP_MASK); outb(SEQ_DATA, 0x0F);

    for (uint32_t i = 0; i < size; i++) {
        volatile uint8_t dummy = framebuf[src + i]; (void)dummy;
        framebuf[i] = 0;
    }

    outb(GC_ADDR, GC_MODE);    outb(GC_DATA, 0x00);
    gc_set_color(bg_color);
    gc_set_bitmask(0xFF);

    uint32_t last_row = (uint32_t)((ROWS - 1) * FONT_H * BYTES_PER_ROW);
    for (uint32_t i = 0; i < (uint32_t)(FONT_H * BYTES_PER_ROW); i++) {
        volatile uint8_t dummy = framebuf[last_row + i]; (void)dummy;
        framebuf[last_row + i] = 0xFF;
    }

    gc_reset();
}

// ========================================================================
// Инициализация DAC палитры
// ========================================================================
static void init_palette(void) {
    for (int i = 0; i < 16; i++) {
        outb(0x3C8, i);
        outb(0x3C9, ega_pal[i][0]);
        outb(0x3C9, ega_pal[i][1]);
        outb(0x3C9, ega_pal[i][2]);
    }
}

