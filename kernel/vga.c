void vga_init(void) {
    gc_reset();
    init_palette();
    cur_x = 0; cur_y = 0;
    fg_color = 15; bg_color = 0;
    vga_clear();
}

void vga_clear(void) {
    outb(GC_ADDR, GC_MODE);    outb(GC_DATA, 0x00);
    gc_set_color(bg_color);
    gc_set_bitmask(0xFF);

    for (uint32_t i = 0; i < (uint32_t)(BYTES_PER_ROW * SCREEN_H); i++) {
        volatile uint8_t dummy = framebuf[i]; (void)dummy;
        framebuf[i] = 0xFF;
    }
    gc_reset();
    cur_x = 0; cur_y = 0;
}

void vga_set_color(vga_color_t fg, vga_color_t bg) {
    fg_color = (uint8_t)fg & 0x0F;
    bg_color = (uint8_t)bg & 0x0F;
}

void vga_putchar(char c) {
    if      (c == '\n') { cur_x = 0; cur_y++; }
    else if (c == '\r') { cur_x = 0; }
    else if (c == '\t') { cur_x = (cur_x + 8) & ~7; }
    else if (c == '\b') { vga_backspace(); return; }
    else {
        draw_char(cur_x, cur_y, (unsigned char)c);
        if (++cur_x >= COLS) { cur_x = 0; cur_y++; }
    }
    if (cur_y >= ROWS) { scroll_up(); cur_y = ROWS - 1; }
}

void vga_backspace(void) {
    if (cur_x > 0) cur_x--;
    else if (cur_y > 0) { cur_y--; cur_x = COLS - 1; }
    erase_char(cur_x, cur_y);
}

void vga_puts(const char* str)  { while (*str) vga_putchar(*str++); }

void vga_puts_color(const char* str, vga_color_t fg, vga_color_t bg) {
    uint8_t sf = fg_color, sb = bg_color;
    vga_set_color(fg, bg); vga_puts(str);
    fg_color = sf; bg_color = sb;
}

void vga_put_dec(uint32_t v) {
    if (!v) { vga_putchar('0'); return; }
    char b[12]; int i = 0;
    while (v) { b[i++] = '0' + v % 10; v /= 10; }
    while (--i >= 0) vga_putchar(b[i]);
}

void vga_put_hex(uint32_t v) {
    static const char h[] = "0123456789ABCDEF";
    vga_puts("0x");
    for (int i = 28; i >= 0; i -= 4) vga_putchar(h[(v>>i)&0xF]);
}

void vga_set_cursor(int x, int y) {
    if (x>=0 && x<COLS && y>=0 && y<ROWS) { cur_x=x; cur_y=y; }
}
void vga_get_cursor(int* x, int* y) { if(x)*x=cur_x; if(y)*y=cur_y; }

void vga_newline(void) {
    cur_x = 0; cur_y++;
    if (cur_y >= ROWS) { scroll_up(); cur_y = ROWS - 1; }
}
void vga_scroll(void) {
    if (cur_y >= ROWS) { scroll_up(); cur_y = ROWS - 1; }
}

void vga_enable_cursor(uint8_t cs, uint8_t ce) { (void)cs; (void)ce; }
void vga_disable_cursor(void) {}
void vga_update_cursor(void)  {}
void vga_restore_palette(void) { init_palette(); }